/**
* RENDER
* Async factory: builds the full component wrapper for the default edit view.
*
* When render_level === 'content', the wrapper shell is skipped and only the
* content_data node is returned. This is used by the component refresh mechanism
* to swap inner content without rebuilding the outer frame.
*
* The label node is suppressed (wrapper_options.label = null) in 'line' view
* because the grid layout already provides a column header.
*
* @param {Object} self - Component instance (see VIEW_DEFAULT_EDIT_AV above for properties).
* @param {Object} options - Render options.
* @param {string} [options.render_level='full'] - 'full' builds the complete wrapper;
*   'content' returns only the content_data node.
* @returns {Promise<HTMLElement>} The .wrapper_component element, or the .content_data
*   element when render_level === 'content'.
*/
view_default_edit_av.render = async function(self, options) {

	// options
		const render_level = options.render_level || 'full'

	// content_data
		const content_data = get_content_data_edit(self)
		if (render_level==='content') {
			return content_data
		}

	// buttons
		const buttons = (self.permissions > 1)
			? get_buttons(self)
			: null

	// wrapper. ui build_edit returns component wrapper
		const wrapper_options = {
			content_data	: content_data,
			buttons			: buttons,
			add_styles		: ['media_wrapper'] // common media classes
		}
		if (self.view==='line') {
			wrapper_options.label = null // prevent to create label node
		}
		const wrapper = ui.component.build_wrapper_edit(self, wrapper_options)
		// set pointers to content_data
		wrapper.content_data = content_data


	return wrapper
}//end render



/**
* GET_CONTENT_DATA_EDIT
* Builds the content_data container and schedules per-entry content_value
* nodes via dd_request_idle_callback so that each entry is rendered in its
* own browser idle task, avoiding long-task jank on sections with many entries.
*
* Each built content_value is appended to content_data and also stored as a
* numeric index pointer (content_data[i]) for later programmatic access by
* refresh logic.
*
* The guard `if (self.status==='destroyed')` inside each callback prevents
* stale idle tasks from touching a component that was torn down before the
* task ran — important for fast navigation between records.
*
* (!) entries is normalised to [null] when empty so that at least one
*     content_value slot is always rendered, giving the operator a visible
*     upload affordance even for empty records.
*
* @param {Object} self - Component instance.
* @returns {HTMLElement} The .content_data.media_content_data container element.
*/
const get_content_data_edit = function(self) {

	// short vars
		const data		= self.data || {}
		const entries	= data.entries || []

	// content_data
		const content_data = ui.component.build_content_data(self)
		// common media classes
		content_data.classList.add('media_content_data')

	// values (inputs)
		const inputs_value		= (entries.length>0) ? entries : [null] // force one empty input at least
		const entries_length	= inputs_value.length
		for (let i = 0; i < entries_length; i++) {
			// force use new separate task
			dd_request_idle_callback(
				() => {
					// status check. If instance is destroyed, return
					if (self.status==='destroyed') return

					const content_value = (self.permissions===1)
						? get_content_value(i, inputs_value[i], self)
						: get_content_value(i, inputs_value[i], self)
					content_data.appendChild(content_value)
					// set pointer
					content_data[i] = content_value
				}
			)
		}


	return content_data
}//end get_content_data_edit



/**
* GET_CONTENT_VALUE
* Builds a single .content_value element for one AV entry.
*
* Rendering strategy:
*  1. A <img class="posterframe"> is always rendered first as a static fallback
*     while the video is not yet available or not in the viewport.
*  2. When a processed video file is found (`file_info.file_exist === true`), a
*     <video> element is created in the hidden state and placed in front of the
*     poster image via prepend(). A viewport observer (when_in_viewport) delays
*     setting video.src until the element is scrolled into view, conserving
*     browser network resources. Once visible, the posterframe <img> is removed
*     from the DOM because the video element's own poster attribute takes over.
*  3. When no video file is found, the posterframe image becomes a clickable
*     affordance that opens tool_upload so the operator can supply the file.
*  4. In 'print' view (self.view === 'print'), only the posterframe is returned;
*     no interactive video or quality selector is built.
*
* Poster image error handling: if the posterframe URL fails to load (e.g. no
* posterframe has been generated yet), the src falls back to
* page_globals.fallback_image; the fallback itself is never retried to prevent
* infinite error loops.
*
* networkState interpretation (HTML Media spec §4.8.11.5):
*   0 = NETWORK_EMPTY, 1 = NETWORK_IDLE, 2 = NETWORK_LOADING, 3 = NETWORK_NO_SOURCE
*   Values > 2 indicate that the browser cannot load the resource.
*
* Cache busting: all media URLs receive a `?t=<timestamp>` query string so that
* browsers do not serve a stale cached file after a re-transcode or posterframe
* regeneration.
*
* (!) The permissions===1 branch currently calls the same get_content_value path
*     as the edit branch. The conditional is a documented no-op placeholder for
*     a future read-only rendering variant. See commented-out
*     get_content_value_read below.
*
* @param {number} i - Zero-based entry index within self.data.entries.
* @param {Object|null} current_value - The entry object at index i, or null for
*   an empty record. When non-null, must carry a `files_info` array of file records
*   with shape: { quality, file_path, file_exist, extension }.
* @param {Object} self - Component instance.
* @returns {HTMLElement} The .content_value.media_content_value container element.
*/
const get_content_value = (i, current_value, self) => {

	// media url from files_info based on selected context quality
		const quality			= self.quality || self.context?.features?.quality
		const data				= self.data || {}
		const files_info		= current_value && current_value.files_info
			? current_value.files_info
			: []
		const file_info			= files_info.find(el => el.quality===quality)
		const posterframe_url	= data.posterframe_url
			? data.posterframe_url + '?t=' + (new Date()).getTime()
			: page_globals.fallback_image

	// content_value
		const content_value = ui.create_dom_element({
			element_type	: 'div',
			class_name 		: 'content_value media_content_value'
		})

	// posterframe_img
		const posterframe_img = ui.create_dom_element({
			element_type	: 'img',
			class_name		: 'posterframe',
			parent			: content_value
		})
		posterframe_img.setAttribute('height', 392)
		posterframe_img.addEventListener('error', function(e) {
			if (posterframe_img.src!==page_globals.fallback_image) {
				posterframe_img.src = page_globals.fallback_image
			}
		})
		// set posterframe_img source URL
		posterframe_img.src = posterframe_url

		// image background color
			// posterframe_img.addEventListener('load', set_bg_color, false)
			// function set_bg_color() {
			// 	this.removeEventListener('load', set_bg_color, false)
			// 	ui.set_background_image(this, content_value)
			// }

	// view_print case. No video is generated
		if (self.view === 'print') {
			return content_value
		}

	// video
		const video_url	= file_info && file_info.file_exist===true
			? DEDALO_MEDIA_URL + file_info.file_path + '?t=' + (new Date()).getTime()
			: null
		if (video_url) {

			// render the video node
			const video = build_video_node(
				posterframe_url
			)
			// fix pointer
			content_value.video = video
			// append node to content_value
			content_value.prepend(video)

			// fix pointer to allow play/pause
			self.video = video

			// observer. Set video node only when it is in viewport (to save browser resources)
			when_in_viewport(
				content_value, // node to observe
				() => { // callback function returns int timestamp

					// remove posterframe image node (video posterframe is set and we don't need it anymore)
						posterframe_img.remove()

					// error event
						video.addEventListener('error', function(e) {

							// debug
								if(SHOW_DEBUG===true) {
									console.warn('e:', e);
									console.warn('this.networkState MediaError:', this.networkState);
								}

							// if the error is caused by the video not loading
							// @see https://html.spec.whatwg.org/multipage/media.html#error-codes
								if (this.networkState > 2) {
									// add an image default
									this.setAttribute('poster', page_globals.fallback_image)
									// notify
									console.error('Network error ['+this.networkState+'] loading video:', e.target);
								}
						}, true)

					video.preload = 'metadata' // load only metadata (duration, dimensions) not full video
					video.src = video_url
					video.classList.remove('hide')
				}
			)
		}else{

			posterframe_img.classList.add('link')
			// mousedown event
			const mousedown_handler = (e) => {
				e.stopPropagation();

				const tool_upload = self.tools.find(el => el.model==='tool_upload')
				// open_tool (tool_common)
				open_tool({
					tool_context	: tool_upload,
					caller			: self
				})
			}
			posterframe_img.addEventListener('mousedown', mousedown_handler)
		}

	// quality_selector
		const quality_selector = get_quality_selector(content_value, self)
		content_value.appendChild(quality_selector)


	return content_value
}//end get_content_value



