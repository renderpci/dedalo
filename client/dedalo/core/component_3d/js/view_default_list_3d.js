/**
* VIEW_DEFAULT_LIST_3D
* Client-side list view for component_3d — renders the 3D component as a
* thumbnail image inside a standard list-row wrapper.
*
* This module provides two exports:
*   - `view_default_list_3d`   — the view constructor (namespace object / no-op).
*   - `get_content_data`       — the content subtree builder, exported so it can
*                                be reused by other list views (e.g. search results).
*
* Rendering strategy:
*   In list mode the component does NOT embed the Three.js viewer.  Instead, it
*   displays the 'thumb' quality still image (generated by the server when the
*   3D file is first uploaded) with a click/mousedown handler that opens the
*   full interactive viewer in a separate window via `handler_open_viewer`.
*   If no thumb exists the posterframe is used as fallback; if that too is
*   absent, `page_globals.fallback_image` is shown.
*
* Data contract (self.data):
*   {
*     entries        : Array<FileInfoObject>  // flat list — only 'default' and
*                                             // 'thumb' qualities are included
*                                             // in list mode (server filters via
*                                             // get_list_value())
*     posterframe_url: string|null            // server-rendered still or null
*   }
*
*   FileInfoObject shape (each element of entries):
*   {
*     quality    : string  // e.g. 'thumb', 'default', 'original'
*     file_path  : string  // media-relative path, prepend DEDALO_MEDIA_URL
*     file_exist : boolean // true when the physical file is confirmed on disk
*     extension  : string  // file extension, e.g. 'glb'
*     file_name  : string
*     file_size  : number
*   }
*/
