 * in the committed config/bootstrap.php which uses env_loader.
 *
 * Parameters are read from environment variables (all optional except where noted):
 *  DD_ROOT           absolute path to the Dédalo root (default: dirname,3 of this file)
 *  DD_PRIVATE_DIR    absolute path to the private dir (default: dirname,3 of this file + '/../private')
 *  DD_DB_HOST        default 127.0.0.1
 *  DD_DB_PORT        default 5432
 *  DD_DB_NAME        default dedalo
 *  DD_DB_USER        default dedalo
 *  DD_DB_PASS        REQUIRED (no sample-default fallback)
 *  DD_ENTITY         default dedalo_local
 *  DD_SALT           default: random 32-hex
 *  DD_INFO           default: "Dédalo local <timestamp>"
 *  DD_INSTALL_STATUS "installed" | "false"  (default installed, for seeded DB)
 *  DD_FORCE          "1" to overwrite existing files
 *  DD_SESSIONS_PATH  absolute path for sessions dir (default: skip)
 *  DD_BACKUP_PATH    absolute path for backups dir (default: skip)
 *  DD_BINARY_BASE_PATH  default /usr/bin
 *
 * @package Dedalo
 * @subpackage Desktop
 */

function env_or(string $key, ?string $default) : ?string {
	$v = getenv($key);
	return ($v === false || $v === '') ? $default : $v;
}

