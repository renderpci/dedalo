/**
 * Write a file unless it exists and $force is false.
 */
function put(string $file, string $contents, bool $force) : void {
	if (file_exists($file) && !$force) {
		fwrite(STDOUT, "skip (exists): " . basename($file) . "\n");
		return;
	}
	if (file_put_contents($file, $contents) === false) {
		fwrite(STDERR, "Failed to write $file\n");
		exit(1);
	}
	fwrite(STDOUT, "wrote: " . basename($file) . "\n");
}

