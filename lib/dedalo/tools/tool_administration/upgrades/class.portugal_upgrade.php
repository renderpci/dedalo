	/**
	* CONVERT_SECTION_DATO_TO_DATA
	* @return
	*/
	public static function convert_section_dato_to_data( stdClass $datos_column ) {

		$dato = clone $datos_column;
		
		// values and dataframe containers
		$section_id_map = portugal_upgrade::get_section_id_map();

		foreach ($dato->relations as $locator) {

			if($locator->section_tipo === 'pt1'){

				$old_section_id = (int) $locator->section_id;

				$new_section_id = array_find($section_id_map, function($el) use ($old_section_id){
					return $el->before === $old_section_id;
					});
				if(empty($new_section_id)){
					debug_log(__METHOD__." OLD pt1 section_id without correspondence in the new pt1: ".to_string( $locator ), logger::ERROR);
				}
				$locator->section_id = (string) $new_section_id->after;
			}
		}

		#dump($dato, ' dato ++ '.to_string());
		#dump(json_encode($dato), ' dato json encoded ++ '.to_string());

		return $dato;
	}//end convert_section_dato_to_data


	/**
	* CONVERT_TM_DATO_TO_DATA
	* @return
	*/
	public static function convert_tm_dato_to_data( $dato ) {

		// values and dataframe containers
		$section_id_map = portugal_upgrade::get_section_id_map();

		foreach ($dato as $locator) {

			if(is_object($locator) && isset($locator->section_tipo) && $locator->section_tipo === 'pt1'){

				$old_section_id = (int) $locator->section_id;

				$new_section_id = array_find($section_id_map, function($el) use ($old_section_id){
					return $el->before === $old_section_id;
					});
				if(empty($new_section_id)){
					debug_log(__METHOD__." OLD pt1 section_id without correspondence in the new pt1: ".to_string( $locator ), logger::ERROR);
				}
				$locator->section_id = (string) $new_section_id->after;
			}
		}

		#dump($dato, ' dato ++ '.to_string());
		#dump(json_encode($dato), ' dato json encoded ++ '.to_string());

		return $dato;
	}//end convert_tm_dato_to_data



	/**
	* CONVERT_TABLE_DATA
	* @return 
	*/
	public static function convert_table_data($ar_tables_json=null) {

		$ar_tables = (array)json_decode($ar_tables_json);
		
		if ($ar_tables===null) {
			// default		
			$ar_tables = [
				"matrix",
				"matrix_activities",
				"matrix_activity",
				"matrix_hierarchy",
				"matrix_hierarchy_main",
				"matrix_langs",
				"matrix_layout",
				"matrix_list",
				"matrix_notes",
				"matrix_profiles",
				"matrix_projects",
				//"matrix_test",
				"matrix_users",
				"matrix_indexations",
				"matrix_structurations",
				"matrix_dataframe",
				"matrix_dd",
				"matrix_layout_dd",
				"matrix_time_machine"
			];
		}

		foreach ($ar_tables as $key => $table) {
			
			// Get last id in the table
			$strQuery 	= "SELECT id FROM $table ORDER BY id DESC LIMIT 1 ";
			$result 	= JSON_RecordDataBoundObject::search_free($strQuery);
			$rows 		= pg_fetch_assoc($result);
			if (!$rows) {
				continue;
			}
			$max = $rows['id'];

			$min = 1;
			if ($table==='matrix_users') {
				$min = -1;
			}
		
			// iterate from 1 to last id
			$i_ref = 0; $start_time=microtime(1);
			for ($i=$min; $i<=$max; $i++) {
				
				if($table === 'matrix_time_machine'){
					$strQuery 	= "SELECT * FROM $table WHERE id = $i ORDER BY id ASC";
				}else{
					$strQuery 	= "SELECT * FROM $table WHERE id = $i ORDER BY id ASC";
				}
				$result 	= JSON_RecordDataBoundObject::search_free($strQuery);
				if(!$result) {			
					$msg = "Failed Search id $i. Data is not found.";	
					debug_log(__METHOD__." ERROR: $msg ".to_string(), logger::ERROR);
					continue;
				}
				$n_rows = pg_num_rows($result);

				if ($n_rows<1) continue;

				while($rows = pg_fetch_assoc($result)) {

					$id				= $rows['id'];
					$datos			= ($table === 'matrix_time_machine') ? json_decode($rows['dato']) : json_decode($rows['datos']);
					$section_tipo	= $rows['section_tipo'];
						#dump($datos, ' datos ++ '.to_string($id));

					if($section_tipo === 'pt1' && $table === 'matrix_hierarchy'){

						// values and dataframe containers
						$section_id_map = portugal_upgrade::get_section_id_map();

						$section_id		= (int) $rows['section_id'];
						debug_log(__METHOD__." section_id ".to_string($section_id), logger::DEBUG);

						$old_section_id_map	= array_find($section_id_map, function($el) use ($section_id){
							return $el->after === $section_id;
						});

						debug_log(__METHOD__." old_section_id_map ".to_string($old_section_id_map), logger::DEBUG);

						if(empty($old_section_id_map)){
							continue;
						}

						$old_indexation = array_find(portugal_upgrade::$ar_old_idenxations, function($el) use ($old_section_id_map){
						return $el->section_id === $old_section_id_map->before;
						});
						debug_log(__METHOD__." old_indexation ".to_string($old_indexation), logger::DEBUG);

						if(empty($old_indexation)){
							continue;
						}

						foreach ($old_indexation->locators as $indexation) {
							$datos->relations[] = $indexation;
						}

						$section_data_encoded 	= json_encode($datos);

						$strQuery 	= "UPDATE $table SET datos = $1 WHERE id = $2 ";
						$result 	= pg_query_params(DBi::_getConnection(), $strQuery, array( $section_data_encoded, $id ));
						if(!$result) {
							$msg = "Failed Update section_data $i";
							debug_log(__METHOD__." ERROR: $msg ".to_string(), logger::ERROR);
							continue;
						}

					}

					if($section_tipo === 'pt1' || $section_tipo === 'pt2'){

						continue;
					}
					if (!empty($datos) && ($table !== 'matrix_time_machine')) {
						$section_data 			= self::convert_section_dato_to_data( $datos );
						$section_data_encoded 	= json_encode($section_data);
			
						$strQuery 	= "UPDATE $table SET datos = $1 WHERE id = $2 ";
						$result 	= pg_query_params(DBi::_getConnection(), $strQuery, array( $section_data_encoded, $id ));
						if(!$result) {			
							$msg = "Failed Update section_data $i";
							debug_log(__METHOD__." ERROR: $msg ".to_string(), logger::ERROR);
							continue;
						}
					} else if (!empty($datos) && $table === 'matrix_time_machine'){

						if(!is_array($datos)){
							continue;
						}

						$section_data 			= self::convert_tm_dato_to_data( $datos );
						$section_data_encoded 	= json_encode($section_data);

						$strQuery 	= "UPDATE $table SET dato = $1 WHERE id = $2 ";
						$result 	= pg_query_params(DBi::_getConnection(), $strQuery, array( $section_data_encoded, $id ));
						if(!$result) {
							$msg = "Failed Update section_data $i";
							debug_log(__METHOD__." ERROR: $msg ".to_string(), logger::ERROR);
							continue;
						}
					}
					else{
						debug_log(__METHOD__." ERROR: Empty datos from: $table - $id ".to_string(), logger::ERROR);
					}					
				}

				// log info each 1000
					if ($i_ref===0) {
						debug_log(__METHOD__." Partial update of section data table: $table - id: $id - total: $n_rows - total time secs: ".exec_time_unit($start_time,'sec'), logger::DEBUG);							
					}else{
						$i_ref = ($i_ref>1000) ? 0 : $i_ref + 1;
					}			
			}
			#break; // stop now
		}//end foreach ($ar_tables as $key => $table)


		return true;
	}//end convert_table_data


