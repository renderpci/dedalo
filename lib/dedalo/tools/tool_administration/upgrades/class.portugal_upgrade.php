	/**
	* IMPORT_PT1_AND_PT2
	* @return
	*/
	public static function save_indexation() {

		// search the terms with indexations
		$strQuery 	= PHP_EOL.sanitize_query('
						SELECT * FROM "matrix_hierarchy"
						WHERE (section_tipo=\'pt1\')
						AND
						(datos#>\'{relations}\' @> \'[{"from_component_tipo":"hierarchy40"}]\')
						ORDER BY section_id ASC'
					);

		$result 	= JSON_RecordDataBoundObject::search_free($strQuery);

		debug_log(__METHOD__." indexations to save: ".to_string($result), logger::DEBUG);


		if(!$result) {
			$msg = "Failed Search of indexation. Data is not found in mattrix_hierarchy.";
			debug_log(__METHOD__." ERROR: $msg ".to_string(), logger::DEBUG);
			// nothing to do.
			return;
		}
		$n_rows = pg_num_rows($result);

		if ($n_rows<1) return; // nothing to do

		while($rows = pg_fetch_assoc($result)) {

			$indexations = new stdClass();

			$datos			= json_decode($rows['datos']);
			$section_tipo	= $rows['section_tipo'];
			$section_id		= (int)$rows['section_id'];

			$ar_idexations_locators = [];
			foreach ($datos->relations as $locator) {

				if($locator->from_component_tipo === 'hierarchy40'){
					$ar_idexations_locators[] = $locator;
				}
			}

			$indexations->section_id 	= $section_id;
			$indexations->locators 		= $ar_idexations_locators;

			portugal_upgrade::$ar_old_idenxations[] = $indexations;
		}

		debug_log(__METHOD__." ar_old_idenxations to save: ". to_string(portugal_upgrade::$ar_old_idenxations ), logger::DEBUG);

	}//end import_pt1_and_pt2



